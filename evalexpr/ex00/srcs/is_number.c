#include "ft.h"

int		is_number(char *str)
{
	int i;
	int len;

	i = 0;
	len = 0;
	if (*str == '-')
		str++;
	while (*str && ('0' <= *str && *str <= '9'))
	{
		len++;
		str++;
	}
	if (len > 0)
		return (1);
	return (0);
}
