int		ft_nbrlen(int nbr)
{
	int len;

	len = 0;
	if (nbr < 0)
		len++;
	if (nbr == 0)
		return (1);
	while (nbr)
	{
		len++;
		nbr /= 10;
	}
	return (len);
}
