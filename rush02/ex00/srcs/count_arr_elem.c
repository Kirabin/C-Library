#include "ft.h"

void	count_arr_elem(char **arr, int *i, int *j)
{
	while (arr[*i])
	{
		*j = 0;
		while (arr[*i][*j] != '\0')
		{
			*j += 1;
		}
		*i += 1;
	}
}
